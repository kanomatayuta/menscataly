/** テンプレートセクション */
export interface SupplementTemplateSection {
  /** セクションID */
  id: string
  /** H2見出し */
  heading: string
  /** H3サブセクション見出し */
  subsections: string[]
  /** セクション概要（執筆指示） */
  writingGuide: string
  /** E-E-A-T要素 */
  eeatElements: string[]
  /** コンプライアンス注記 */
  complianceNotes: string[]
  /** ASP CTA配置の有無 */
  hasCTA: boolean
  /** CTA種別 */
  ctaVariant?: 'primary' | 'secondary'
}

/** サプリメントテンプレート全体 */
export interface SupplementArticleTemplate {
  /** テンプレート名 */
  name: string
  /** カテゴリ */
  category: 'supplement'
  /** PR表記テンプレート */
  prDisclosure: string
  /** 目標文字数 */
  targetWordCount: number
  /** セクション構成 */
  sections: SupplementTemplateSection[]
  /** ASP CTA配置ガイドライン */
  ctaGuidelines: string[]
  /** 全体コンプライアンスルール */
  globalComplianceRules: string[]
  /** E-E-A-T必須チェックリスト */
  eeatChecklist: string[]
  /** 健康食品表現ルール */
  healthFoodClaimRules: string[]
}

