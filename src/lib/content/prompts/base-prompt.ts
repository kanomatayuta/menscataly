/** 共通コンプライアンス指示 */
const COMPLIANCE_INSTRUCTIONS = `
## 薬機法・景表法・ステマ規制の遵守事項（必須）

以下の規制に必ず従ってください。違反した場合は記事を掲載できません。

### 薬機法第66条・67条（誇大広告・承認前広告禁止）
- NG: 「確実に治る」「必ず発毛する」「100%効果がある」
  → OK: 「効果が期待できる」「〜の可能性があります」「〜とされています（個人差があります）」
- NG: 「副作用なし」「完全に安全」「副作用ゼロ」
  → OK: 「副作用のリスクが低いとされています（個人差があります）」
- NG: 「最短〇日で治る」（根拠なき具体的期間の断定）
  → OK: 「個人差はありますが、早期に効果を実感される方もいます」
- NG: 「完治」「根治」（疾患の治癒断定）
  → OK: 「症状の改善」「進行を抑制」

### 景表法（優良誤認・有利誤認・最大級表現禁止）
- NG: 「最安値」「業界No.1」「日本一」（根拠なき場合）
  → OK: 「調査時点（YYYY年MM月DD日）での最安水準」「〇〇調査によるNo.1（YYYY年MM月実施）」
- NG: 「完全無料」（条件がある場合）
  → OK: 「初回無料（※条件は公式サイト参照）」
- NG: 根拠のない統計（「〇〇人中〇〇人に効果」等）
  → OK: 「〇〇調査（YYYY年MM月、n=〇〇）によると〇〇人中〇〇人に効果」

### ステマ規制（2023年10月施行）
- 記事冒頭に必ずPR・アフィリエイト表記を挿入してください
- 「純粋な口コミ」「忖度なしのレビュー」等、広告を隠蔽する表現は禁止です
- アフィリエイトリンクには rel="sponsored" を付与してください（実装時に自動対応）
`.trim();

