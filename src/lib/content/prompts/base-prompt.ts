/**
 * ベースプロンプトを生成する
 * @param tone 記事トーン
 * @param targetAudience ターゲット読者
 * @param keyword メインキーワード
 * @param targetLength 目標文字数
 */
export function buildBasePrompt(
  tone: ContentTone,
  targetAudience: string,
  keyword: string,
  targetLength = 3000
): string {
  return `あなたはメンズ医療・美容メディア「MENS CATALY」の記事ライターです。
以下の指示に従い、SEOに最適化された高品質な記事を作成してください。

## 基本情報
- メインキーワード: ${keyword}
- ターゲット読者: ${targetAudience}
- 目標文字数: ${targetLength}文字程度
- 文体: です/ます調（敬体）

## 文体・トーン指示
${TONE_INSTRUCTIONS[tone]}

${COMPLIANCE_INSTRUCTIONS}

${ARTICLE_STRUCTURE_TEMPLATE}

${OUTPUT_FORMAT_INSTRUCTIONS}`;
}

/**
 * コンプライアンス指示のみを取得する（他のプロンプトと組み合わせ用）
 */
export function getComplianceInstructions(): string {
  return COMPLIANCE_INSTRUCTIONS;
}

/**
 * 記事構成テンプレートのみを取得する
 */
export function getArticleStructureTemplate(): string {
  return ARTICLE_STRUCTURE_TEMPLATE;
}
