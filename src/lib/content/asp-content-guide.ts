// ============================================================
// ユーティリティ関数
// ============================================================

/**
 * カテゴリ別のASPコンテンツガイドを取得する
 * @param category コンテンツカテゴリ
 */
export function getContentGuide(category: ContentCategory): AspContentGuide {
  return ASP_CONTENT_GUIDES[category]
}

/**
 * カテゴリ別のコンプライアンス準拠CTAフレーズを取得する
 * @param category コンテンツカテゴリ
 * @returns コンプライアンス準拠フレーズの配列
 */
export function getSafeCTAs(category: ContentCategory): string[] {
  return ASP_CONTENT_GUIDES[category].complianceSafePhrases
}
