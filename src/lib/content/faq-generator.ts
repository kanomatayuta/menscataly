/**
 * 同じテーマに対する質問バリエーション
 * ランダム選択で FAQ の多様性を向上させる
 */
interface FAQVariation {
  /** バリエーション質問テンプレート群 */
  questions: string[]
  /** バリエーション回答テンプレート群 */
  answers: string[]
  /** FAQ カテゴリ */
  faqCategory: string
}

/**
 * バリエーションから1つの質問・回答ペアをランダム選択する
 * シード付きの擬似ランダム（キーワードベース）で再現性を確保
 */
function selectVariation(variation: FAQVariation, keyword: string, index: number): FAQItem {
  // キーワードとインデックスからシンプルなハッシュ値を生成（再現性確保）
  let hash = 0
  const seed = keyword + String(index)
  for (let i = 0; i < seed.length; i++) {
    hash = ((hash << 5) - hash + seed.charCodeAt(i)) | 0
  }
  const qIdx = Math.abs(hash) % variation.questions.length
  const aIdx = Math.abs(hash + 1) % variation.answers.length

  return {
    question: variation.questions[qIdx].replace(/\{\{KEYWORD\}\}/g, keyword),
    answer: variation.answers[aIdx].replace(/\{\{KEYWORD\}\}/g, keyword),
    faqCategory: variation.faqCategory,
  }
}

