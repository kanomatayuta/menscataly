// ============================================================
// 型定義
// ============================================================

/** FAQ アイテム */
export interface FAQItem {
  /** 質問テキスト */
  question: string
  /** 回答テキスト */
  answer: string
  /** FAQ カテゴリ（質問の分類） */
  faqCategory: string
}

/** FAQ 生成結果 */
export interface FAQGenerationResult {
  /** キーワード */
  keyword: string
  /** カテゴリ */
  category: string
  /** 生成された FAQ アイテムリスト */
  items: FAQItem[]
  /** FAQ構造化データ (JSON-LD) */
  structuredData: Record<string, unknown>
  /** FAQ の Markdown テキスト */
  markdown: string
  /** FAQ の HTML テキスト */
  html: string
}

/** カテゴリ別 FAQ テンプレート */
interface CategoryFAQTemplate {
  /** カテゴリ ID */
  category: string
  /** FAQ テーマ（質問のカテゴリ分類） */
  themes: string[]
  /** テンプレート質問・回答ペア */
  templates: FAQItem[]
}

